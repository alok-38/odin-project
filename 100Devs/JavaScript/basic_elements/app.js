// A variable to store a name
let name = "John";
// A function to greet the user
function greetUser() {
    alert(`Hello ${name}!`)
}

// Another function to add two numbers
function addTwoNumbers(num1, num2) {
    // Add the numbers
    return num1 + num2;
}

// Call the function
greetUser();
let result = addTwoNumbers(5, 3);
alert(result);